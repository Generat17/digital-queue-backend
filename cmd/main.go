package main

import (
	"context"
	"github.com/joho/godotenv"
	_ "github.com/lib/pq"
	"github.com/sirupsen/logrus"
	"github.com/spf13/viper"
	"os"
	"os/signal"
	"server"
	"server/pkg/handler"
	"server/pkg/repository"
	"server/pkg/service"
	"server/types"
	"syscall"
)

// @title       Digital Queue API
